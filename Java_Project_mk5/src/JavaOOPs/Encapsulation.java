package JavaOOPs;

public class Encapsulation {
	
	private   String name = "";
	private  int age = 0;
	
	public  void setName(String name1) {
		this.name = name1;
	}
	
	public  void setAge(int age1) {
		this.age = age1;
	}
	
	public  String getName() {
		return name;
	}
	
	public  int getAge() {
		return age;
	}
	

}
