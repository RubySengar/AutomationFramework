package JavaOOPs;

public class Inheritance {
	
	static String name 	= "";
	static String Class 	= "";
	static String Category = "";
	static String type 	= "";
	static String power 	= "";
	
	public static void Animal() {
		 
		System.out.println("Animals name is >>> " + name);
		System.out.println("Animals Class is >>> " + Class);
	}
	
	public void Description() {
		
		System.out.println("Animals Category is >>> " + Category);
		System.out.println("Animals type is >>> " + 	type);
		System.out.println("Animals power is >>> " + 	power);
		
	}

}
